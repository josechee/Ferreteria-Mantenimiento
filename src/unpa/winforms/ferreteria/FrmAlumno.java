    private void LimpiarCampos() {
        if (alumno != null) {
            alumno = new Alumno();
        }
        this.txtMatricula.setText("");
        this.txtNombre.setText("");
        this.txtApPaterno.setText("");
        this.txtApMaterno.setText("");
        this.txtDireccion.setText("");
        this.txtTelefono.setText("");
        this.alumno.setActivo(0);
        //this.txtActivo.setText("");
    }

