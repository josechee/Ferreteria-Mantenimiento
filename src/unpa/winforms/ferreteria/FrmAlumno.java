    private void consultar() throws Exception {
        this.LimpiarCampos();
        this.alumno.setMatricula(matriculaAux);
        this.alumno = testuniversity.AlumnoQry(this.alumno);
        System.out.println("probando en el metodo consultar" + alumno.getNombre());
        this.txtNombre.setText(alumno.getNombre());
        this.txtApPaterno.setText(alumno.getAp_Paterno());
        this.txtApMaterno.setText(alumno.getAp_Materno());
        this.txtDireccion.setText(alumno.getDireccion());
        this.txtTelefono.setText(alumno.getTelefono());
        System.out.println(alumno.getMatricula());
    }

    private void actualizar() throws Exception {
        int verificador = 0;
        //this.matriculaAux=this.txtMatricula.getText();
        alumno.setMatricula(this.matriculaAux);
        if (this.LlenarAtributos()) {
            verificador = testuniversity.AlumnoUpd(alumno);

            System.out.println("probabdo = " + verificador);

            if (verificador == 1) {
                JOptionPane.showMessageDialog(this, "Datos Actualizados");
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo Actualizar");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Verifica tus datos");
        }
    }

    private void eliminarLogicamente() throws Exception {
        int verificador;
        // alumno.setMatricula(this.txtMatricula.getText());
        alumno.setMatricula(this.matriculaAux);
        if (this.checBoxActivo.isSelected() == true) {
            alumno.setActivo(1);
        } else {
            alumno.setActivo(0);
        }
        verificador = testuniversity.AlumnoELogic(alumno);
        if (verificador == 1) {
            JOptionPane.showMessageDialog(this, "Eliminacion Exitosa");
        } else {
            JOptionPane.showMessageDialog(this, "Eliminacion Fallida");
        }
    }
