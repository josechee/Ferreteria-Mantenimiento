    //comprobar que si sean los dijitos entre 7- 10 de numero telefonico y que sean letras
    private void txtNumeroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumeroKeyReleased
        Pattern pat = Pattern.compile("[0-9\\s]{7,10}");
        Matcher mat = pat.matcher(this.txtNumero.getText().trim());
        if (mat.matches()) {
            this.txtNumero.setBackground(null);
            this.areaNumero = true;
        } else {
            this.txtNumero.setBackground(Color.RED);
            this.areaNumero = false;
        }
    }//GEN-LAST:event_txtNumeroKeyReleased

