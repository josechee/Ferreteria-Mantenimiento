        String fNacimiento[] = empleado.getFechaNacimiento().split("/");
        this.jComboDia.setSelectedIndex(Integer.parseInt(fNacimiento[0]) - 1);
        this.jComboMes.setSelectedIndex(Integer.parseInt(fNacimiento[1]) - 1);
        this.comboxEmpleado.setSelectedIndex(tipoEmpleado - 1);
        this.txtAño.setText(fNacimiento[2]);

