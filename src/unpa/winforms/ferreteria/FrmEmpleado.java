    private boolean ValidarCampos() {
        boolean validar = false;
        if (!this.txtNombreEmpleado.getText().isEmpty()
                && !this.txtApellidoPaterno.getText().isEmpty()
                && !this.txtApellidoMaterno.getText().isEmpty()
                && !this.txtAño.getText().isEmpty()
                && !this.txtUsuario.getText().isEmpty()
                && !this.txtContraseña.getText().isEmpty()
                && !this.txtCalle.getText().isEmpty()
                && !this.txtColonia.getText().isEmpty()
                && !this.txtCiudad.getText().isEmpty()
                && !this.txtCodigoPostal.getText().isEmpty()
                && !this.txtLada.getText().isEmpty()
                && !this.txtNumero.getText().isEmpty()) {
            validar = true;
        } else {
            validar = false;
        }
        return validar;
    }

    private boolean LlenarAtributos() {
        boolean verificador = false;
        String mesSeleccionado = String.valueOf(this.jComboMes.getSelectedIndex() + 1);
        String diaSeleccionado = String.valueOf(this.jComboDia.getSelectedIndex() + 1);
        String fechaNacimiento = diaSeleccionado + "/" + mesSeleccionado + "/" + this.txtAño.getText();
        System.out.println("diaSeleccionado " + diaSeleccionado);
        System.out.println("mesSeleccionado" + mesSeleccionado);
        System.out.println("fechaNacimiento" + fechaNacimiento);

        int indiceComboxTipoEmplead = this.comboxEmpleado.getSelectedIndex();
        empleado = new Empleado();
        this.usuario = new Usuario();
        if (this.ValidarCampos()) {
            System.out.println(this.txtNombreEmpleado.getText());
            empleado.setId_TipoEmpleado(indiceComboxTipoEmplead + 1);
            empleado.setNombre(this.txtNombreEmpleado.getText());
            empleado.setApPaterno(this.txtApellidoPaterno.getText());
            empleado.setApMaterno(this.txtApellidoMaterno.getText());
            empleado.setFechaNacimiento(fechaNacimiento);
            empleado.setCalle(this.txtCalle.getText());
            empleado.setColonia(this.txtColonia.getText());
            empleado.setCiudad(this.txtCiudad.getText());
            empleado.setCodigoPostal(Integer.parseInt(this.txtCodigoPostal.getText()));
            usuario.setCuentaUsuario(this.txtUsuario.getText());
            usuario.setContraseña(this.txtContraseña.getText());
            verificador = true;
        } else {
            JOptionPane.showMessageDialog(this, "Completar campos vacios");
            verificador = false;
        }
        return verificador;
    }

